"""RAG 提示词模板加载。"""

DEFAULT_RAG_PROMPT = """你是一个严谨的知识库问答助手。
请基于给定的参考资料回答用户问题。此阶段只负责总结知识库内容，后续最终回答会单独判断是否补充通用知识。要求：
1) 只能基于参考资料作答，不要编造。
2) 若资料不足，明确说明“知识库暂无足够信息”并给出可补充的信息点。
3) 回答简洁、结构清晰，优先给结论，再给依据。
4) 若参考资料含「关联图示」：在对应步骤/段落说明的**下一行**插入 `[KB_IMAGE:绝对路径]`（路径来自参考资料，勿改写）；禁止只在文末集中罗列、禁止省略标记。

用户问题：
{query}

参考资料：
{context}
"""


DEFAULT_RAG_FLOW_PROMPT = """你是一个严谨的知识库流程还原助手。
请基于给定的参考资料，按知识库原顺序完整回答用户问题。要求：
1) 用户问流程、步骤、教程、配置、安装、操作方法时，不要只摘要；必须尽量完整保留主流程、步骤、命令、参数、注意事项。
2) 严格按参考资料顺序组织回答；不要自行重排步骤。
3) 若某一步后有 `[KB_IMAGE:绝对路径]`，必须在该步骤说明后立即原样插入该标记；不要集中放在文末，不要改写路径。
4) 只压缩重复寒暄或无关背景，不要省略操作步骤、关键配置、表格内容和错误处理。
5) 若资料不足以覆盖完整流程，明确说明缺少哪一段。

用户问题：
{query}

参考资料：
{context}
"""


def load_rag_prompt(project_directory: str) -> str:
    """功能：返回默认知识库问答提示词模板。
    参数：
    - project_directory：项目根目录（预留扩展自定义模板路径）。
    返回值：
    - str：含 `{query}`、`{context}` 占位符的提示词文本。
    """
    return DEFAULT_RAG_PROMPT


def load_rag_flow_prompt(project_directory: str) -> str:
    """功能：返回流程/步骤类问题的专用提示词模板。
    参数：
    - project_directory：项目根目录（预留扩展自定义模板路径）。
    返回值：
    - str：强调按顺序还原步骤的提示词文本。
    """
    return DEFAULT_RAG_FLOW_PROMPT
